<?php

namespace Jelix\Authentication\Account;

class Account
{
    protected $login;

    protected $email;

    protected $status;

    public function __construct()
    {

    }

    public function getLogin()
    {
        return $this->login;
    }

    public function getEmail()
    {
        return $this->login;
    }

    public function getStatus()
    {
        return $this->login;
    }
}